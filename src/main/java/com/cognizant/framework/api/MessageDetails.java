package com.cognizant.framework.api;

public class MessageDetails {

	private String id;
	private String threadId;
	
	
	//Getter and Setter
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getThreadId() {
		return threadId;
	}
	public void setThreadId(String threadId) {
		this.threadId = threadId;
	}
	

	
	
}
